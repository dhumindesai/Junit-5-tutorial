public class MathUtils {

    public int add(int a, int b){
        return a+b;
    }

    public double divide(int a, int b){
        return a/b;
    }

    public double calculateCircleArea(double radius){
        return Math.PI * radius * radius;
    }

    public int multiply(int a, int b) {
        return a*b;
    }
}
